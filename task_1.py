"""
Задание 1.

Запросите у пользователя значения выручки и издержек фирмы.
Определите, с каким финансовым результатом работает фирма
(прибыль — выручка больше издержек, или убыток — издержки больше выручки).
Выведите соответствующее сообщение. Если фирма отработала с прибылью,
вычислите рентабельность выручки (соотношение прибыли к выручке).
Далее запросите численность сотрудников фирмы и определите
прибыль фирмы в расчете на одного сотрудника.

Пример:
Введите выручку фирмы: 1000
Введите издержки фирмы: 500
Финансовый результат - прибыль. Ее величина: 500
Значит вычисляем рентабельность выручки (соотношение прибыли к выручке)
Рентабельность выручки = 0.5
Введите численность сотрудников фирмы: 10
Прибыль фирмы в расчете на одного сотрудника = 50.0

Обязательно решите с применением f-строк и if else
"""
# Запрашиваем у пользователя значения выручки и издержек
revenue = float(input("Введите выручку фирмы: "))
costs = float(input("Введите издержки фирмы: "))

# Определяем финансовый результат
if revenue > costs:
    profit = revenue - costs  # Вычисляем прибыль
    print("Фирма работает с прибылью.")

    # Вычисляем рентабельность выручки
    profitability = profit / revenue * 100  # Выражаем в процентах
    print(f"Рентабельность выручки составляет {profitability:.2f}%")
else:
    print("Фирма работает с убытком.")

# Запрашиваем численность сотрудников
employees = int(input("Введите численность сотрудников фирмы: "))

# Проверяем, что численность сотрудников больше 0
if employees > 0:
    profit_per_employee = profit / employees if revenue > costs else 0  # Прибыль на одного сотрудника
    print(f"Прибыль фирмы в расчете на одного сотрудника: {profit_per_employee:.2f}")
else:
    print("Численность сотрудников должна быть больше 0.")